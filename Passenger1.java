
class Passenger1 {
    private String name;
    private int age;
    private char gender;

    public Passenger1(String name, int age, char gender) {
        this.name = name;
        this.age = age;
        this.gender = gender;
    }

    // Getters for Passenger attributes (for demonstration purposes)
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public char getGender() {
        return gender;
    }
}